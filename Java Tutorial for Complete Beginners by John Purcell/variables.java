public class variables {

    public static void main(String[] args) {

        int num1 = 12;
        int num2 = 21;
        int sum = num1 + num2;
        byte num3 = 127;
        double num4 = 12.2725;
        float num5 = 12.24f;
        char mychar = 'a';
        boolean myboolean = true;
        System.out.println("The sum of num1 and num2 is:" + sum);
        System.out.println("The byte number is:" + num3);
        System.out.println("The double number is:" + num4);
        System.out.println("The float number is:" + num5);
        System.out.println("The character is:" + mychar);
        System.out.println("The boolean value is:" + myboolean);
    }
}

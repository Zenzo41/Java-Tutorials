public class whileloop {
   public static void main(String[] args)
   {
       int i=1;
       int num=50;
       System.out.println("The value of num before loop is:"+num);
   
       while(i<=10)
       {
           num=num+2;
            i++;
           
       }
       System.out.println("The value of num after loop is:"+num);
   } 
}
